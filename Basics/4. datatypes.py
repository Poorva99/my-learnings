# 1. Numbers
# a) Integer
a = 5
print(type(a))
# b) Float
a = 5.0
print(type(a))
# c) Complex
a = 5 + 6j
print(type(a))

# 2. String
a = "paro"
b = 'poorva'
print(type(a), type(b))

# 3. Boolean
a = True
b = False
print(type(a), type(b))

# 4. None
a = None
print(type(a))

# 5. List
a = [1, 2, 3, 'paro', ['a', 'b', 'c'], True]
print(type(a))

# 6. Tuple
a = (1, 2, 3, 'paro', ['a', 'b', 'c'], True)
print(type(a))

# 7. Dictionary
a = {'key': 'value'}  # JSON
print(type(a))

# 8. Set - unique elements
a = {1,2,3,4,5,6,7,7,7,7,7,7,7,7}
print(a)
print(type(a))